package com.example.ticketingsystem;

public class SimulationStatus {
    private int totalTicketsProduced;
    private int totalTicketsRemoved;


    public int getTotalTicketsProduced() {
        return totalTicketsProduced;
    }

    public void setTotalTicketsProduced(int totalTicketsProduced) {
        this.totalTicketsProduced = totalTicketsProduced;
    }

    public int getTotalTicketsRemoved() {
        return totalTicketsRemoved;
    }

    public void setTotalTicketsRemoved(int totalTicketsRemoved) {
        this.totalTicketsRemoved = totalTicketsRemoved;
    }

}
